#!/usr/bin/env bash
# Cron-friendly: produce a daily summary of the TaskFlow store and write it
# to .claude/.logs/daily-summary.md.
#
# Example crontab entry (08:00 daily):
#   0 8 * * * cd /path/to/taskflow && bash scripts/scheduled-summary.sh
#
# Combine with `claude --print` to have an LLM rewrite it as a stand-up message.

set -euo pipefail

LOG_DIR=".claude/.logs"
mkdir -p "$LOG_DIR"
OUT="$LOG_DIR/daily-summary.md"
DB="${TASKFLOW_DB:-.taskflow.json}"

if [ ! -f "$DB" ]; then
  echo "no taskflow db at $DB — nothing to summarize" >&2
  exit 0
fi

DATE="$(date -Iseconds)"
STATS="$(node "$PWD/src/cli/index.js" stats 2>/dev/null || echo '(stats unavailable)')"
RECENT_DONE="$(node "$PWD/src/cli/index.js" list --done true 2>/dev/null | tail -5 || echo '(none)')"
OPEN_HIGH="$(node "$PWD/src/cli/index.js" list --done false --priority high 2>/dev/null || echo '(none)')"

cat > "$OUT" <<EOF
# Daily summary — $DATE

## Stats
\`\`\`
$STATS
\`\`\`

## Recently completed (last 5)
\`\`\`
$RECENT_DONE
\`\`\`

## Open high-priority
\`\`\`
$OPEN_HIGH
\`\`\`

> Generated by scripts/scheduled-summary.sh
EOF

echo "wrote $OUT"

# Optional: ask Claude (headless) to convert it to a stand-up message
if [ "${CLAUDE_REWRITE:-0}" = "1" ] && command -v claude >/dev/null 2>&1; then
  claude --print --permission-mode plan \
    "Rewrite the markdown in $OUT as a 4-line Slack stand-up message. Lead with what shipped, then risks." \
    > "$LOG_DIR/daily-standup.txt"
  echo "wrote $LOG_DIR/daily-standup.txt"
fi
